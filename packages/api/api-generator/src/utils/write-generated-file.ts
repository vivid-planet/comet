import { ESLint } from "eslint";
import { promises as fs } from "fs";
import * as path from "path";

export async function writeGeneratedFile(filePath: string, contents: string): Promise<void> {
    const header = `// This file has been generated by comet api-generator.
    // You may choose to use this file as scaffold by moving this file out of generated folder and removing this comment.
    `;
    await fs.mkdir(path.dirname(filePath), { recursive: true });
    await fs.writeFile(filePath, contents);

    const eslint = new ESLint({
        cwd: process.cwd(),
        fix: true,
    });
    const lintResult = await eslint.lintText(header + contents, {
        filePath,
    });

    const output = lintResult[0] && lintResult[0].output ? lintResult[0].output : lintResult[0].source;
    if (output) {
        await fs.writeFile(filePath, output);
    }
    console.log(`generated ${filePath}`);
}
